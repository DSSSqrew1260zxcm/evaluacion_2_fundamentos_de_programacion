package eva2_24_arreglos_ciclos;

public class Eva2_24_arreglos_ciclos {

    public static void main(String[] args) {
        int[] arreglo = new int[10];
        //primero = 0; ultimo = n-1
        for (int i = 0; i < 10; i++) {
            arreglo[i] = 10000;
        }
        for (int i = 0; i < 10; i++) {
            arreglo[i] = 10000;
            System.out.println(arreglo[i]);
        }
    }

}
